-- ============================================================
-- 预算执行审计领域测试数据库 Schema + 测试数据
-- ============================================================

DROP TABLE IF EXISTS three_public_expense;
DROP TABLE IF EXISTS special_fund;
DROP TABLE IF EXISTS fiscal_revenue;
DROP TABLE IF EXISTS budget_execution;
DROP TABLE IF EXISTS budget_indicator;

-- 预算指标表
CREATE TABLE budget_indicator (
    id INT PRIMARY KEY AUTO_INCREMENT,
    fiscal_year INT NOT NULL COMMENT '财政年度',
    region_code VARCHAR(20) NOT NULL COMMENT '行政区划代码',
    region_name VARCHAR(50) NOT NULL COMMENT '行政区划名称',
    dept_code VARCHAR(20) NOT NULL COMMENT '部门代码',
    dept_name VARCHAR(100) NOT NULL COMMENT '部门名称',
    budget_type VARCHAR(20) NOT NULL COMMENT '预算类型：一般公共预算/政府性基金预算/国有资本经营预算',
    expense_category VARCHAR(50) NOT NULL COMMENT '支出功能分类：教育/医疗/社保/农林水/交通/科技/文化/公共安全',
    annual_budget DECIMAL(14,2) NOT NULL COMMENT '年初预算(万元)',
    adjusted_budget DECIMAL(14,2) COMMENT '调整后预算(万元)',
    budget_source VARCHAR(20) COMMENT '资金来源：中央/省级/市级/县级'
) COMMENT='预算指标表';

-- 预算执行明细表
CREATE TABLE budget_execution (
    id INT PRIMARY KEY AUTO_INCREMENT,
    fiscal_year INT NOT NULL COMMENT '财政年度',
    region_code VARCHAR(20) NOT NULL COMMENT '行政区划代码',
    region_name VARCHAR(50) NOT NULL COMMENT '行政区划名称',
    dept_code VARCHAR(20) NOT NULL COMMENT '部门代码',
    dept_name VARCHAR(100) NOT NULL COMMENT '部门名称',
    expense_category VARCHAR(50) NOT NULL COMMENT '支出功能分类',
    budget_amount DECIMAL(14,2) NOT NULL COMMENT '预算金额(万元)',
    executed_amount DECIMAL(14,2) NOT NULL COMMENT '已执行金额(万元)',
    payment_date DATE COMMENT '支付日期',
    payment_type VARCHAR(20) COMMENT '支付方式：直接支付/授权支付',
    voucher_number VARCHAR(50) COMMENT '凭证号',
    payee VARCHAR(100) COMMENT '收款方',
    execution_status VARCHAR(20) DEFAULT '正常' COMMENT '状态：正常/暂停/终止',
    quarter INT COMMENT '所属季度：1/2/3/4'
) COMMENT='预算执行明细表';

-- 财政收入表
CREATE TABLE fiscal_revenue (
    id INT PRIMARY KEY AUTO_INCREMENT,
    fiscal_year INT NOT NULL COMMENT '财政年度',
    region_code VARCHAR(20) NOT NULL COMMENT '行政区划代码',
    region_name VARCHAR(50) NOT NULL COMMENT '行政区划名称',
    revenue_type VARCHAR(30) NOT NULL COMMENT '收入类型：税收收入/非税收入/转移支付/债务收入',
    revenue_category VARCHAR(50) NOT NULL COMMENT '收入科目：增值税/企业所得税/个人所得税/土地出让金/行政事业性收费/罚没收入/国有资源收入',
    planned_amount DECIMAL(14,2) NOT NULL COMMENT '计划收入(万元)',
    actual_amount DECIMAL(14,2) NOT NULL COMMENT '实际收入(万元)',
    collection_month INT COMMENT '征收月份',
    collection_status VARCHAR(20) DEFAULT '已入库' COMMENT '状态：已入库/待入库/退库'
) COMMENT='财政收入表';

-- 专项资金表
CREATE TABLE special_fund (
    id INT PRIMARY KEY AUTO_INCREMENT,
    fiscal_year INT NOT NULL COMMENT '财政年度',
    region_code VARCHAR(20) NOT NULL COMMENT '行政区划代码',
    region_name VARCHAR(50) NOT NULL COMMENT '行政区划名称',
    fund_name VARCHAR(100) NOT NULL COMMENT '专项资金名称',
    fund_type VARCHAR(30) NOT NULL COMMENT '资金类型：中央转移支付/省级专项/市级配套',
    total_amount DECIMAL(14,2) NOT NULL COMMENT '资金总额(万元)',
    allocated_amount DECIMAL(14,2) COMMENT '已拨付金额(万元)',
    used_amount DECIMAL(14,2) COMMENT '已使用金额(万元)',
    beneficiary_count INT COMMENT '受益人数',
    project_count INT COMMENT '涉及项目数',
    start_date DATE COMMENT '资金下达日期',
    deadline DATE COMMENT '使用截止日期',
    fund_status VARCHAR(20) DEFAULT '执行中' COMMENT '状态：执行中/已结题/已收回',
    audit_flag TINYINT DEFAULT 0 COMMENT '是否已审计：0否1是'
) COMMENT='专项资金表';

-- 三公经费表
CREATE TABLE three_public_expense (
    id INT PRIMARY KEY AUTO_INCREMENT,
    fiscal_year INT NOT NULL COMMENT '财政年度',
    region_code VARCHAR(20) NOT NULL COMMENT '行政区划代码',
    region_name VARCHAR(50) NOT NULL COMMENT '行政区划名称',
    dept_code VARCHAR(20) NOT NULL COMMENT '部门代码',
    dept_name VARCHAR(100) NOT NULL COMMENT '部门名称',
    expense_type VARCHAR(30) NOT NULL COMMENT '三公经费类型：因公出国/公务用车/公务接待',
    budget_amount DECIMAL(12,2) NOT NULL COMMENT '预算金额(万元)',
    actual_amount DECIMAL(12,2) NOT NULL COMMENT '实际支出(万元)',
    last_year_amount DECIMAL(12,2) COMMENT '上年同期支出(万元)',
    person_count INT COMMENT '涉及人次',
    detail_description VARCHAR(500) COMMENT '明细说明'
) COMMENT='三公经费表';

