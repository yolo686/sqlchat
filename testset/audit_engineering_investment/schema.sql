-- ============================================================
-- 工程投资审计领域测试数据库 Schema + 测试数据
-- ============================================================

DROP TABLE IF EXISTS project_settlement;
DROP TABLE IF EXISTS project_change;
DROP TABLE IF EXISTS project_payment;
DROP TABLE IF EXISTS project_contract;
DROP TABLE IF EXISTS project_info;

-- 项目基本信息表
CREATE TABLE project_info (
    id INT PRIMARY KEY AUTO_INCREMENT,
    project_code VARCHAR(30) NOT NULL COMMENT '项目编号',
    project_name VARCHAR(200) NOT NULL COMMENT '项目名称',
    region_code VARCHAR(20) NOT NULL COMMENT '行政区划代码',
    region_name VARCHAR(50) NOT NULL COMMENT '行政区划名称',
    project_type VARCHAR(30) NOT NULL COMMENT '项目类型：道路桥梁/水利工程/市政工程/房建工程/轨道交通',
    investment_type VARCHAR(20) NOT NULL COMMENT '投资类型：政府投资/PPP/BOT/EPC',
    approved_investment DECIMAL(14,2) NOT NULL COMMENT '批复投资额(万元)',
    actual_investment DECIMAL(14,2) COMMENT '实际投资额(万元)',
    funding_source VARCHAR(50) COMMENT '资金来源：中央补助/省级补助/地方自筹/银行贷款/社会资本',
    start_date DATE COMMENT '计划开工日期',
    end_date DATE COMMENT '计划竣工日期',
    actual_start_date DATE COMMENT '实际开工日期',
    actual_end_date DATE COMMENT '实际竣工日期',
    project_status VARCHAR(20) NOT NULL COMMENT '项目状态：前期/在建/完工/验收/结算',
    construction_unit VARCHAR(100) COMMENT '建设单位',
    supervision_unit VARCHAR(100) COMMENT '监理单位',
    progress_pct DECIMAL(5,2) DEFAULT 0 COMMENT '工程进度(%)'
) COMMENT='项目基本信息表';

-- 工程合同表
CREATE TABLE project_contract (
    id INT PRIMARY KEY AUTO_INCREMENT,
    contract_code VARCHAR(30) NOT NULL COMMENT '合同编号',
    project_code VARCHAR(30) NOT NULL COMMENT '项目编号',
    contract_name VARCHAR(200) NOT NULL COMMENT '合同名称',
    contract_type VARCHAR(30) NOT NULL COMMENT '合同类型：施工/监理/设计/勘察/设备采购/材料采购',
    contractor VARCHAR(100) NOT NULL COMMENT '承包方/供应商',
    contract_amount DECIMAL(14,2) NOT NULL COMMENT '合同金额(万元)',
    signed_date DATE COMMENT '签订日期',
    contract_period_days INT COMMENT '合同工期(天)',
    procurement_method VARCHAR(20) COMMENT '采购方式：公开招标/邀请招标/竞争性谈判/单一来源/询价',
    bid_price DECIMAL(14,2) COMMENT '中标价(万元)',
    bid_control_price DECIMAL(14,2) COMMENT '招标控制价(万元)',
    warranty_amount DECIMAL(12,2) COMMENT '质保金(万元)',
    contract_status VARCHAR(20) DEFAULT '执行中' COMMENT '状态：执行中/已完成/已终止'
) COMMENT='工程合同表';

-- 工程付款表
CREATE TABLE project_payment (
    id INT PRIMARY KEY AUTO_INCREMENT,
    payment_code VARCHAR(30) NOT NULL COMMENT '付款编号',
    project_code VARCHAR(30) NOT NULL COMMENT '项目编号',
    contract_code VARCHAR(30) COMMENT '合同编号',
    payment_type VARCHAR(30) NOT NULL COMMENT '付款类型：进度款/预付款/结算款/质保金退还/其他',
    payment_amount DECIMAL(14,2) NOT NULL COMMENT '付款金额(万元)',
    payment_date DATE NOT NULL COMMENT '付款日期',
    payee VARCHAR(100) NOT NULL COMMENT '收款方',
    approval_date DATE COMMENT '审批日期',
    approver VARCHAR(50) COMMENT '审批人',
    voucher_number VARCHAR(50) COMMENT '凭证号',
    payment_status VARCHAR(20) DEFAULT '已支付' COMMENT '状态：已支付/待审批/已退回'
) COMMENT='工程付款表';

-- 工程变更表
CREATE TABLE project_change (
    id INT PRIMARY KEY AUTO_INCREMENT,
    change_code VARCHAR(30) NOT NULL COMMENT '变更编号',
    project_code VARCHAR(30) NOT NULL COMMENT '项目编号',
    contract_code VARCHAR(30) COMMENT '关联合同编号',
    change_type VARCHAR(30) NOT NULL COMMENT '变更类型：设计变更/工程量增加/工程量减少/工期延长/材料替换/其他',
    change_reason VARCHAR(500) COMMENT '变更原因',
    original_amount DECIMAL(14,2) COMMENT '变更前金额(万元)',
    changed_amount DECIMAL(14,2) COMMENT '变更后金额(万元)',
    change_difference DECIMAL(14,2) COMMENT '变更差额(万元)',
    approval_date DATE COMMENT '审批日期',
    approval_level VARCHAR(20) COMMENT '审批层级：项目部/公司/主管部门',
    change_status VARCHAR(20) DEFAULT '已批准' COMMENT '状态：待审/已批准/已驳回'
) COMMENT='工程变更表';

-- 工程结算表
CREATE TABLE project_settlement (
    id INT PRIMARY KEY AUTO_INCREMENT,
    settlement_code VARCHAR(30) NOT NULL COMMENT '结算编号',
    project_code VARCHAR(30) NOT NULL COMMENT '项目编号',
    contract_code VARCHAR(30) COMMENT '合同编号',
    contract_amount DECIMAL(14,2) NOT NULL COMMENT '合同金额(万元)',
    change_amount DECIMAL(14,2) DEFAULT 0 COMMENT '变更金额(万元)',
    claimed_amount DECIMAL(14,2) NOT NULL COMMENT '送审金额(万元)',
    audited_amount DECIMAL(14,2) COMMENT '审定金额(万元)',
    reduction_amount DECIMAL(14,2) COMMENT '核减金额(万元)',
    reduction_rate DECIMAL(6,2) COMMENT '核减率(%)',
    settlement_date DATE COMMENT '结算日期',
    auditor VARCHAR(50) COMMENT '审计人员',
    settlement_status VARCHAR(20) DEFAULT '审核中' COMMENT '状态：审核中/已完成/已退回'
) COMMENT='工程结算表';

