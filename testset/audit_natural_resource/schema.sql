-- ============================================================
-- 自然资源审计领域测试数据库 Schema + 测试数据
-- ============================================================

DROP TABLE IF EXISTS eco_redline;
DROP TABLE IF EXISTS water_resource;
DROP TABLE IF EXISTS land_transfer;
DROP TABLE IF EXISTS mineral_resource;
DROP TABLE IF EXISTS land_use;

-- 土地利用现状表
CREATE TABLE land_use (
    id INT PRIMARY KEY AUTO_INCREMENT,
    region_code VARCHAR(20) NOT NULL COMMENT '行政区划代码',
    region_name VARCHAR(50) NOT NULL COMMENT '行政区划名称',
    land_type VARCHAR(30) NOT NULL COMMENT '土地类型：耕地/林地/草地/建设用地/未利用地/水域',
    area_hectare DECIMAL(12,2) NOT NULL COMMENT '面积(公顷)',
    survey_year INT NOT NULL COMMENT '调查年份',
    ownership_type VARCHAR(20) COMMENT '权属类型：国有/集体',
    quality_grade VARCHAR(10) COMMENT '质量等级：优/良/中/差',
    is_basic_farmland TINYINT DEFAULT 0 COMMENT '是否基本农田：0否1是',
    last_update DATE COMMENT '最后更新日期'
) COMMENT='土地利用现状表';

-- 矿产资源表
CREATE TABLE mineral_resource (
    id INT PRIMARY KEY AUTO_INCREMENT,
    mine_name VARCHAR(100) NOT NULL COMMENT '矿山名称',
    region_code VARCHAR(20) NOT NULL COMMENT '行政区划代码',
    region_name VARCHAR(50) NOT NULL COMMENT '行政区划名称',
    mineral_type VARCHAR(30) NOT NULL COMMENT '矿种：铁矿/铜矿/金矿/煤矿/石灰石/花岗岩/砂石',
    mine_status VARCHAR(20) NOT NULL COMMENT '状态：在采/停采/关闭/筹建',
    license_number VARCHAR(50) COMMENT '采矿许可证号',
    license_start DATE COMMENT '许可证起始日期',
    license_end DATE COMMENT '许可证截止日期',
    approved_capacity DECIMAL(10,2) COMMENT '设计产能(万吨/年)',
    actual_output DECIMAL(10,2) COMMENT '实际产量(万吨/年)',
    remaining_reserve DECIMAL(12,2) COMMENT '剩余储量(万吨)',
    mine_area_hectare DECIMAL(8,2) COMMENT '矿区面积(公顷)',
    environmental_deposit DECIMAL(12,2) COMMENT '环境治理保证金(万元)',
    restore_area DECIMAL(8,2) COMMENT '已恢复治理面积(公顷)'
) COMMENT='矿产资源表';

-- 土地出让表
CREATE TABLE land_transfer (
    id INT PRIMARY KEY AUTO_INCREMENT,
    transfer_code VARCHAR(50) NOT NULL COMMENT '出让编号',
    region_code VARCHAR(20) NOT NULL COMMENT '行政区划代码',
    region_name VARCHAR(50) NOT NULL COMMENT '行政区划名称',
    land_location VARCHAR(200) COMMENT '土地坐落',
    land_use_type VARCHAR(30) NOT NULL COMMENT '用途：住宅/商业/工业/综合',
    transfer_method VARCHAR(20) NOT NULL COMMENT '出让方式：招标/拍卖/挂牌/协议',
    area_sqm DECIMAL(12,2) NOT NULL COMMENT '出让面积(平方米)',
    transfer_price DECIMAL(14,2) NOT NULL COMMENT '成交价(万元)',
    floor_price DECIMAL(10,2) COMMENT '起始价(万元)',
    premium_rate DECIMAL(6,2) COMMENT '溢价率(%)',
    buyer_name VARCHAR(100) COMMENT '受让人',
    transfer_date DATE NOT NULL COMMENT '成交日期',
    payment_deadline DATE COMMENT '缴款截止日期',
    actual_payment_date DATE COMMENT '实际缴款日期',
    contract_status VARCHAR(20) COMMENT '合同状态：已签/未签/违约',
    planned_start_date DATE COMMENT '约定开工日期',
    actual_start_date DATE COMMENT '实际开工日期',
    idle_months INT DEFAULT 0 COMMENT '闲置月数'
) COMMENT='土地出让表';

-- 水资源管理表
CREATE TABLE water_resource (
    id INT PRIMARY KEY AUTO_INCREMENT,
    region_code VARCHAR(20) NOT NULL COMMENT '行政区划代码',
    region_name VARCHAR(50) NOT NULL COMMENT '行政区划名称',
    water_source_name VARCHAR(100) NOT NULL COMMENT '水源名称',
    water_type VARCHAR(20) NOT NULL COMMENT '类型：河流/湖泊/水库/地下水',
    total_volume DECIMAL(12,2) COMMENT '总水量(万立方米)',
    allocated_volume DECIMAL(12,2) COMMENT '已分配水量(万立方米)',
    actual_usage DECIMAL(12,2) COMMENT '实际用水量(万立方米)',
    water_quality_level VARCHAR(10) COMMENT '水质等级：I/II/III/IV/V/劣V',
    monitoring_year INT COMMENT '监测年份',
    license_holder VARCHAR(100) COMMENT '取水许可证持有者',
    permitted_volume DECIMAL(12,2) COMMENT '许可取水量(万立方米)',
    water_fee_rate DECIMAL(6,2) COMMENT '水资源费标准(元/立方米)',
    water_fee_collected DECIMAL(12,2) COMMENT '已征收水资源费(万元)',
    water_fee_due DECIMAL(12,2) COMMENT '应征收水资源费(万元)'
) COMMENT='水资源管理表';

-- 生态保护红线表
CREATE TABLE eco_redline (
    id INT PRIMARY KEY AUTO_INCREMENT,
    region_code VARCHAR(20) NOT NULL COMMENT '行政区划代码',
    region_name VARCHAR(50) NOT NULL COMMENT '行政区划名称',
    redline_type VARCHAR(30) NOT NULL COMMENT '红线类型：水源保护区/自然保护区/风景名胜区/湿地/森林公园',
    redline_name VARCHAR(100) NOT NULL COMMENT '保护区名称',
    total_area_hectare DECIMAL(12,2) NOT NULL COMMENT '总面积(公顷)',
    core_area_hectare DECIMAL(12,2) COMMENT '核心区面积(公顷)',
    buffer_area_hectare DECIMAL(12,2) COMMENT '缓冲区面积(公顷)',
    protection_level VARCHAR(20) COMMENT '保护级别：国家级/省级/市级',
    establish_year INT COMMENT '设立年份',
    violation_count INT DEFAULT 0 COMMENT '违规项目数',
    violation_area_hectare DECIMAL(10,2) DEFAULT 0 COMMENT '违规占用面积(公顷)',
    inspection_date DATE COMMENT '最近检查日期',
    inspection_result VARCHAR(20) COMMENT '检查结果：合格/不合格/整改中'
) COMMENT='生态保护红线表';

